// Операторы отношения Relational Operators
console.log(5 > 10);    // false
console.log(5 < 10);    // true
console.log(10 >= 10);  // true
console.log(8 <= 10);   // true
console.log(10 === 10); // true
console.log(10 !== 10); // false
console.log(10 == "10"); // true приведение типов
console.log(10 === "10"); // false


