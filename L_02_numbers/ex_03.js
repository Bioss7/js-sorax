// Арифметические операции

// Унарный + и - 
console.log(+10);
console.log(-10);

// Операторы инкремента и декремента, побочный эффект увеличивают или уменьшают
var i = 10;
console.log(++i);   // Префиксный инкремент 
console.log(i);
console.log(i++);   // Постфиксный инкремент
console.log(i);     // 12

// Бинарные операторы
console.log(5 + 10);
console.log(7- 3);
console.log(5 * 9);
console.log(10 / 3);
console.log(10 % 3);

var n = 100;
n += 20;
console.log(n);
n *= 2; 
console.log(n);

